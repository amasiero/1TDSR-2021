class Variaveis {
  public static void main(String[] args) {
    // Para declarar uma variável no Java.
    // Precisa determinar qual o tipo de valor que ela vai ter (armazenar).
    // Java possui tipos primitivos e tipos por referência.
    /**
     * Tipos Primitivos São 8 tipos: - byte - short - int - long -> até aqui são
     * números inteiros. - float - double -> até aqui são números reais (com
     * vírgula). - char -> caracter alfanumérico - boolean -> verdadeiro e falso
     * 
     */

    // A variável numeroInteiro recebe o valor 10
    int numeroInteiro = 10;

    int a = 5;
    int b;
    b = 20;

    int c = a + b;
    System.out.println(c);

    // Tipo por referência -> String -> texto
    String texto = "Hello World";
    System.out.println(texto);

  }
}