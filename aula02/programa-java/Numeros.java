class Numeros {
  public static void main(String[] args) {
    System.out.println("Hello " + "World");

    // Operações básicas da Matemática (Operações Aritméticas)
    System.out.println(1 + 1); // soma
    System.out.println(1 - 1); // subtração
    System.out.println(2 * 2); // multiplicação
    System.out.println(2 / 2); // divisão
    System.out.println(3 % 2); // Mod -> Resto da divisão

    // Pacote de Matemática -> Trabalhar com operações complexas
    System.out.println(Math.pow(3, 2)); // Potencia
    System.out.println(Math.sqrt(25)); // Raiz quadrada
    System.out.println(Math.PI); // Constante PI
  }
}